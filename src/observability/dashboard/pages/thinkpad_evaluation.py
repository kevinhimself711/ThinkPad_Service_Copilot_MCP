"""ThinkPad evaluation report viewer.

This page is intentionally read-only. M6 evaluation runs are executed through
scripts/thinkpad_evaluate.py so the dashboard never triggers provider calls,
index writes, or answer generation.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import streamlit as st

DEFAULT_REPORT_PATH = Path("data/eval/m6_report.json")


def render() -> None:
    """Render a lightweight ThinkPad M6 evaluation report viewer."""

    st.header("ThinkPad Evaluation")
    st.markdown("Review the latest local ThinkPad M6 evaluation report.")

    report_path = Path(
        st.text_input(
            "Report path",
            value=str(DEFAULT_REPORT_PATH),
            help="Read-only JSON report generated by scripts/thinkpad_evaluate.py.",
        )
    )
    if not report_path.exists():
        st.info(f"No ThinkPad evaluation report found at `{report_path}`.")
        st.caption("Run scripts/thinkpad_evaluate.py to generate a local report.")
        return

    try:
        report = json.loads(report_path.read_text(encoding="utf-8"))
    except Exception as exc:
        st.error(f"Failed to load ThinkPad evaluation report: {exc}")
        return

    st.caption(
        f"Version: {report.get('version', 'unknown')} | "
        f"Collection: {report.get('collection', 'unknown')} | "
        f"Cases: {report.get('query_count', 0)}"
    )
    _render_environment(report.get("environment") or {})
    _render_metrics("Aggregate Metrics", report.get("aggregate_metrics") or {})
    _render_category_metrics(report.get("category_metrics") or {})
    _render_failed_cases(report.get("case_results") or [])


def _render_environment(environment: dict[str, Any]) -> None:
    cols = st.columns(4)
    values = [
        ("Manuals", environment.get("manual_count", 0)),
        ("Skipped", environment.get("skipped_case_count", 0)),
        ("Failed", environment.get("failed_case_count", 0)),
        ("Live", "yes" if environment.get("live_retrieval_available") else "no"),
    ]
    for index, (label, value) in enumerate(values):
        with cols[index]:
            st.metric(label, value)


def _render_metrics(title: str, metrics: dict[str, Any]) -> None:
    st.subheader(title)
    if not metrics:
        st.info("No metrics available.")
        return
    rows = [
        {"metric": name, "value": value}
        for name, value in sorted(metrics.items())
        if isinstance(value, int | float)
    ]
    st.dataframe(rows, use_container_width=True)


def _render_category_metrics(category_metrics: dict[str, Any]) -> None:
    st.subheader("Category Metrics")
    if not category_metrics:
        st.info("No category metrics available.")
        return
    rows: list[dict[str, Any]] = []
    for category, metrics in sorted(category_metrics.items()):
        if not isinstance(metrics, dict):
            continue
        row = {"category": category}
        row.update(metrics)
        rows.append(row)
    st.dataframe(rows, use_container_width=True)


def _render_failed_cases(case_results: list[dict[str, Any]]) -> None:
    st.subheader("Failed Or Skipped Cases")
    rows = []
    for result in case_results:
        if result.get("passed") and not result.get("skipped"):
            continue
        rows.append(
            {
                "case_id": result.get("case_id"),
                "category": result.get("category"),
                "tool": result.get("tool"),
                "expected": result.get("expected_status"),
                "actual": result.get("actual_status"),
                "reasons": "; ".join(result.get("failure_reasons") or []),
            }
        )
    if not rows:
        st.success("No failed or skipped cases in this report.")
        return
    st.dataframe(rows, use_container_width=True)
